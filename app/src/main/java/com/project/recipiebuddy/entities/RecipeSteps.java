package com.project.recipiebuddy.entities;

public class RecipeSteps {
	int recipe_id, recipe_sl, recipe_time;
	String recipe_step;
	public int getRecipe_id() {
		return recipe_id;
	}
	public void setRecipe_id(int recipe_id) {
		this.recipe_id = recipe_id;
	}
	public int getRecipe_sl() {
		return recipe_sl;
	}
	public void setRecipe_sl(int recipe_sl) {
		this.recipe_sl = recipe_sl;
	}
	public int getRecipe_time() {
		return recipe_time;
	}
	public void setRecipe_time(int recipe_time) {
		this.recipe_time = recipe_time;
	}
	public String getRecipe_step() {
		return recipe_step;
	}
	public void setRecipe_step(String recipe_step) {
		this.recipe_step = recipe_step;
	}
	
	

}
